"""
LeetCode 190. Reverse Bits
难度: Easy

题目描述：
颠倒给定的 32 位无符号整数的二进制位。

示例 1：
  输入：n = 0b00000010100101000001111010011100 (43261596)
  输出：964176192 (0b00111001011110000010100101000000)
  解释：输入的二进制串反转后得到输出。

示例 2：
  输入：n = 0b11111111111111111111111111111101 (4294967293)
  输出：3221225471 (0b10111111111111111111111111111111)

【拓展练习】
1. LeetCode 191. Number of 1 Bits —— 计算二进制中 1 的个数
2. LeetCode 7. Reverse Integer —— 反转十进制整数
3. LeetCode 338. Counting Bits —— 统计 0 到 n 每个数的二进制 1 的个数
"""


class Solution:
    """
    ==================== 解法一：逐位反转 ====================

    【核心思路】
    从右到左逐位取出 n 的每一位，将其放到结果的对应位置（从左到右）。
    具体做法：结果左移一位腾出位置，然后把 n 的最低位加上去，n 再右移一位。
    重复 32 次。

    【思考过程】
    1. 要把第 0 位放到第 31 位，第 1 位放到第 30 位……本质是逐位搬运。
    2. 每次 result <<= 1 在高位腾出空间，result |= (n & 1) 把 n 的最低位填进去。
    3. 然后 n >>= 1 让 n 的下一位变成最低位。
    4. 循环 32 次，就完成了所有位的搬运。

    【举例】n = 0b1011 (简化为4位)
      i=0: result=0b0, n的最低位=1 → result=0b1, n=0b101
      i=1: result=0b10, n的最低位=1 → result=0b11, n=0b10
      i=2: result=0b110, n的最低位=0 → result=0b110, n=0b1
      i=3: result=0b1100, n的最低位=1 → result=0b1101, n=0b0
      结果: 0b1101 (原来的 0b1011 反转了)

    【时间复杂度】O(1)，固定循环 32 次
    【空间复杂度】O(1)
    """
    def reverseBits_iterative(self, n: int) -> int:
        result = 0
        for _ in range(32):
            result = (result << 1) | (n & 1)
            n >>= 1
        return result

    """
    ==================== 解法二：分治位翻转 ====================

    【核心思路】
    采用分治策略，先交换相邻的 1 位，再交换相邻的 2 位，
    再交换相邻的 4 位、8 位、16 位。5 步完成全部 32 位的反转。

    【思考过程】
    1. 分治法把"反转 32 位"分解为更小的子问题。
    2. 类似于归并排序的思想——先在最小粒度上翻转，再逐层合并。
    3. 用掩码和移位操作实现每一层的交换：
       - 0x55555555 = 01010101... 用于交换相邻 1 位
       - 0x33333333 = 00110011... 用于交换相邻 2 位
       - 0x0F0F0F0F = 00001111... 用于交换相邻 4 位
       - 0x00FF00FF 用于交换相邻 8 位
       - 0x0000FFFF 用于交换相邻 16 位
    4. 每步操作：把偶数位右移、奇数位左移，再或起来。

    【举例】n = 0b ABCD EFGH（简化为8位，每个字母代表1位）
      交换1位: → BADC FEHG（相邻1位互换）
      交换2位: → DCBA HGFE（相邻2位互换）
      交换4位: → HGFE DCBA（相邻4位互换）
      完成反转！

    【时间复杂度】O(1)，固定 5 步操作
    【空间复杂度】O(1)
    """
    def reverseBits_divide(self, n: int) -> int:
        n = ((n & 0x55555555) << 1) | ((n >> 1) & 0x55555555)
        n = ((n & 0x33333333) << 2) | ((n >> 2) & 0x33333333)
        n = ((n & 0x0F0F0F0F) << 4) | ((n >> 4) & 0x0F0F0F0F)
        n = ((n & 0x00FF00FF) << 8) | ((n >> 8) & 0x00FF00FF)
        n = ((n & 0x0000FFFF) << 16) | ((n >> 16) & 0x0000FFFF)
        return n
