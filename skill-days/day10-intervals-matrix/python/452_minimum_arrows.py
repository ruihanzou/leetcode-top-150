"""
LeetCode 452. Minimum Number of Arrows to Burst Balloons
难度: Medium

题目描述：
有一些球形气球贴在一面用 XY 平面表示的墙壁上。墙面上的气球记录在整数数组 points 中，
其中 points[i] = [xstart, xend] 表示水平直径在 xstart 和 xend 之间的气球。
你不知道气球的确切 y 坐标。

一支弓箭可以沿着 x 轴从不同点完全垂直地射出。在坐标 x 处射出一支箭，
若有一个气球的直径的开始和结束坐标为 xstart 和 xend，且满足 xstart <= x <= xend，
则该气球会被引爆。可以射出的弓箭数量没有限制，弓箭一旦被射出之后，可以无限地前进。

返回引爆所有气球所必须射出的最小弓箭数。

示例 1：points = [[10,16],[2,8],[1,6],[7,12]] → 输出 2
  解释：射出 x=6 的箭引爆 [2,8] 和 [1,6]；射出 x=11 的箭引爆 [10,16] 和 [7,12]

示例 2：points = [[1,2],[3,4],[5,6],[7,8]] → 输出 4
  解释：每个气球互不重叠，需要 4 支箭

示例 3：points = [[1,2],[2,3],[3,4],[4,5]] → 输出 2
  解释：射出 x=2 引爆 [1,2] 和 [2,3]；射出 x=4 引爆 [3,4] 和 [4,5]

【拓展练习】
1. LeetCode 435. Non-overlapping Intervals —— 删最少区间使剩余不重叠，互补问题
2. LeetCode 56. Merge Intervals —— 合并重叠区间，区间操作基础
3. LeetCode 253. Meeting Rooms II —— 求最少会议室（最大重叠数），贪心思想类似
"""

from typing import List


class Solution:
    """
    ==================== 解法一：按右端点排序 + 贪心 ====================

    【核心思路】
    按气球区间的右端点从小到大排序。贪心地将箭射在当前气球的右端点，
    这样能尽量多地覆盖后续气球。如果下一个气球的左端点 > 当前箭的位置，
    就需要一支新箭。

    【思考过程】
    1. 这是经典的"活动选择/区间调度"问题的变形。
       问题转化为：找最多的互不重叠区间组数 → 等价于最少的"覆盖点"。

    2. 为什么按右端点排序？
       箭射在右端点意味着"尽量往右延迟"，给后续气球留出更多被覆盖的机会。
       如果射得太靠左，可能本来能一起射掉的气球就覆盖不到了。

    3. 排序后遍历：
       - 第一支箭射在第一个气球的右端点
       - 对于后续每个气球，如果其左端点 <= 当前箭位置 → 被当前箭覆盖
       - 否则需要新箭，射在该气球的右端点

    【举例】points = [[10,16],[2,8],[1,6],[7,12]]
      按右端点排序 → [[1,6],[2,8],[7,12],[10,16]]
      箭1 射在 x=6：
        [1,6] ✓（6 在 [1,6] 内）
        [2,8] ✓（6 在 [2,8] 内）
        [7,12]：7 > 6 → 需要新箭
      箭2 射在 x=12：
        [7,12] ✓（12 在 [7,12] 内）
        [10,16] ✓（12 在 [10,16] 内）
      共 2 支箭

    【时间复杂度】O(n log n)，排序主导
    【空间复杂度】O(1)，原地排序（Timsort 实际 O(n)）
    """
    def findMinArrowShots_by_end(self, points: List[List[int]]) -> int:
        points.sort(key=lambda x: x[1])

        arrows = 1
        arrow_pos = points[0][1]

        for i in range(1, len(points)):
            if points[i][0] > arrow_pos:
                arrows += 1
                arrow_pos = points[i][1]

        return arrows

    """
    ==================== 解法二：按左端点排序 + 贪心 ====================

    【核心思路】
    按气球区间的左端点从小到大排序。维护当前"公共交集"的右端点 end。
    如果下一个气球的左端点 > end，说明无法与当前组共用一支箭，需要新箭；
    否则更新 end = min(end, 当前气球的右端点)，缩小交集。

    【思考过程】
    1. 换个角度：一组气球能被同一支箭射穿 ⟺ 它们的区间有公共交集。
       公共交集的右端点就是所有气球右端点的最小值。

    2. 按左端点排序后，依次考虑每个气球：
       - 如果它的左端点 <= 当前交集的右端点 → 它可以加入当前组
         更新交集右端点为 min(end, point[1])
       - 否则 → 当前组结束，开新组，射新箭

    3. 与解法一对比：
       - 解法一按右端点排序，arrow_pos 是固定的射箭位置
       - 解法二按左端点排序，end 是动态缩小的交集边界
       - 本质相同，都是贪心地将尽量多的气球归入同一组

    【举例】points = [[10,16],[2,8],[1,6],[7,12]]
      按左端点排序 → [[1,6],[2,8],[7,12],[10,16]]
      第1组：end = 6
        [1,6]: 起始，end=6
        [2,8]: 2 <= 6 → 加入，end=min(6,8)=6
        [7,12]: 7 > 6 → 新箭！
      第2组：end = 12
        [7,12]: 起始，end=12
        [10,16]: 10 <= 12 → 加入，end=min(12,16)=12
      共 2 支箭

    【时间复杂度】O(n log n)，排序主导
    【空间复杂度】O(1)
    """
    def findMinArrowShots_by_start(self, points: List[List[int]]) -> int:
        points.sort(key=lambda x: x[0])

        arrows = 1
        end = points[0][1]

        for i in range(1, len(points)):
            if points[i][0] > end:
                arrows += 1
                end = points[i][1]
            else:
                end = min(end, points[i][1])

        return arrows
