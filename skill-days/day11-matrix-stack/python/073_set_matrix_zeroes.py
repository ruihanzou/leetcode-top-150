"""
LeetCode 73. Set Matrix Zeroes
难度: Medium

题目描述：
给定一个 m x n 的矩阵，如果一个元素为 0，则将其所在行和列的所有元素都设为 0。
请使用原地算法。

示例 1：
输入：matrix = [[1,1,1],[1,0,1],[1,1,1]]
输出：[[1,0,1],[0,0,0],[1,0,1]]

示例 2：
输入：matrix = [[0,1,2,0],[3,4,5,2],[1,3,1,5]]
输出：[[0,0,0,0],[0,4,5,0],[0,3,1,0]]

【拓展练习】
1. LeetCode 289. Game of Life —— 类似的"需要同时更新但要避免覆盖"的矩阵问题
2. LeetCode 2123. Minimum Operations to Remove Adjacent Ones in Matrix —— 矩阵标记与影响传播
3. LeetCode 1329. Sort the Matrix Diagonally —— 对矩阵对角线操作的练习
"""

from typing import List


class Solution:
    """
    ==================== 解法一：额外标记数组 ====================

    【核心思路】
    用两个集合 zero_rows 和 zero_cols 分别记录哪些行、哪些列需要置零。
    第一遍扫描标记，第二遍根据标记置零。

    【思考过程】
    1. 不能边遍历边置零：遇到 0 就立刻把整行整列置零，会"污染"其他格子，
       后续遍历时无法区分「原生 0」和「我们刚写的 0」，导致连锁错误（详见解法二图示）。
       → 必须先扫一遍记录「哪些行、列有原生 0」，再统一置零。

    2. 最直观的做法：用两个集合分别记录行和列。
       - 第一遍：扫描矩阵，遇到0就标记 zero_rows.add(i), zero_cols.add(j)。
       - 第二遍：如果 i in zero_rows 或 j in zero_cols，则 matrix[i][j]=0。

    【举例】matrix = [[1,1,1],[1,0,1],[1,1,1]]
      第一遍：遇到 (1,1)=0 → zero_rows={1}, zero_cols={1}
      第二遍：按标记置零
        行 1 全置零 → [0,0,0]
        列 1 全置零 → (0,1),(2,1) 置 0
      结果：[[1,0,1],[0,0,0],[1,0,1]]

    【时间复杂度】O(m*n)
    【空间复杂度】O(m+n)
    """
    def setZeroes_extra(self, matrix: List[List[int]]) -> None:
        m, n = len(matrix), len(matrix[0])
        zero_rows = set()
        zero_cols = set()

        for i in range(m):
            for j in range(n):
                if matrix[i][j] == 0:
                    zero_rows.add(i)
                    zero_cols.add(j)

        for i in range(m):
            for j in range(n):
                if i in zero_rows or j in zero_cols:
                    matrix[i][j] = 0

    """
    ==================== 解法二：用第一行第一列作标记 ====================

    【核心思路】
    将矩阵的第一行和第一列当作标记数组使用，从而省去额外的 O(m+n) 空间。
    但第一行/第一列本身是否需要置零，需要用一个额外变量记录。

    【思考过程】

    1. 为什么不能边遍历边置零？举例说明：
       matrix = [[0,1,1],[1,1,1],[1,1,1]]，只有 (0,0) 是原生 0。
       正确输出：[[0,0,0],[0,1,1],[0,1,1]]

       图示（边遍历边置零的错误过程）：
         原始矩阵（●=原生0）    处理(0,0)后           遍历到(1,0)时误判
           j=0  1  2              j=0  1  2              j=0  1  2
        i=0 [●][1][1]         i=0 [0][0][0]         i=0 [0][0][0]
        i=1 [1][1][1]    →    i=1 [0][1][1]    →    i=1 [0][0][0]  ❌ 整行被错误清零
        i=2 [1][1][1]         i=2 [0][1][1]         i=2 [0][1][1]
                              ↑ (1,0)是我们写的0，      ↑ 误以为第1行有原生0
                                后续遍历会误认为是原生0
       → 必须先扫一遍记录「哪些行、列有原生 0」，再统一置零。

    2. 标记怎么打？把第一行、第一列当作标记数组：
       - 扫描到 (i,j)=0 时，把 matrix[i][0] 和 matrix[0][j] 都置 0
       - matrix[i][0]=0 表示「第 i 行需要置零」
       - matrix[0][j]=0 表示「第 j 列需要置零」

       为什么选第一行/第一列？不是只能选它们，而是选它们最简单：
       - 要记录 m 个行是否置零 → 需要 m 个位置
       - 要记录 n 个列是否置零 → 需要 n 个位置
       - 选第 0 列：matrix[i][0] 有 m 个格子，正好对应 m 行
       - 选第 0 行：matrix[0][j] 有 n 个格子，正好对应 n 列
       - 也可以选第 2 行、第 3 列，但代码要额外记录「用哪行/列存标记」
       - 选第 0 行/列最直观，索引从 0 开始，代码最简洁

       图示（第一行/列作标记数组）：
             j=0    j=1    j=2    j=3
          +------+------+------+------+
       i=0| (0,0)| (0,1)| (0,2)| (0,3)|  ← 第一行：matrix[0][j]=0 表示「第 j 列需置零」
          +------+------+------+------+      (0,0) 还表示「第 0 行需置零」
       i=1| (1,0)|  ·   |  ·   |  ·   |
          +------+------+------+------+  ↑
       i=2| (2,0)|  ·   |  ·   |  ·   |  第一列：matrix[i][0]=0 表示「第 i 行需置零」
          +------+------+------+------+
       标记规则：格子写 0 表示对应行/列需要置零，非 0 表示不需要。

       打标记小例：发现 (1,2)=0 时 → matrix[1][0]=0，matrix[0][2]=0
         打标记前           打标记后
           j=0  1  2          j=0  1  2
        i=0 [1][1][1]     i=0 [1][1][0]  ← [0][2]=0 标记第2列
        i=1 [3][4][0]  →  i=1 [0][4][0]  ← [1][0]=0 标记第1行
        i=2 [1][3][1]     i=2 [1][3][1]      ↑ 原生0 触发

    3. 第一行/列会被覆盖，怎么办？
       格 (0,0) 同时属于第一行和第一列，只能存一个标记。需要拆开：
       - matrix[0][0]   → 标记「第 0 行是否置零」
       - first_col_zero → 标记「第 0 列是否置零」（额外变量）

       问题示例：(0,0) 是原生 0 时，需要同时标记第 0 行和第 0 列。
       若只用 matrix[0][0]=0 表示两件事，后续无法区分：

         情况A：只有第0行有原生0       情况B：只有第0列有原生0       情况C：第0行和第0列都有原生0
         （原生0在(0,1)，列0无0）      （原生0在(1,0)，行0无0）      （原生0在(0,0)）
           j=0  1  2                     j=0  1  2                     j=0  1  2
        i=0 [1][●][1]                 i=0 [1][1][1]                 i=0 [●][1][1]
        i=1 [1][1][1]                 i=1 [●][1][1]                 i=1 [1][1][1]
        i=2 [1][1][1]                 i=2 [1][1][1]                 i=2 [1][1][1]
         只需行0置零                    只需列0置零                    行0和列0都要置零

         三种情况打标记后 matrix[0][0] 都是 0（A: [0][1]=0；B: [1][0]=0；C: [0][0]=0），
         无法区分是「仅行0」「仅列0」还是「都要」。所以 matrix[0][0] 只负责行，列用变量。
         所以：matrix[0][0] 只负责「第 0 行」，第 0 列单独用 first_col_zero 变量记录。

    4. 置零顺序：必须从右下往左上，否则标记会被提前覆盖。

       错误示例（从左上往右下置零）：
         原始：只有 (0,0)、(1,0) 是原生 0，第 2 列无原生 0。
         标记后：matrix[0][0]=0（行0），matrix[1][0]=0（行1），matrix[0][2]=1（列2 不需置零）
           j=0  1  2              置零时从 (0,0) 开始：
        i=0 [0][1][1]  ←标记    i=0 [0][0][0]  ← 第0行置零时，把 matrix[0][2] 从 1 覆盖成 0！
        i=1 [0][4][5]          i=1 [0][4][5]     原本 [0][2]=1 表示「列2不需置零」
        i=2 [1][3][1]          i=2 [1][3][1]
                ↑ [0][2]=1 表示列2无0
         处理第 1、2 行时，读取 matrix[0][2] 发现是 0，误以为第 2 列要置零，
         导致 (1,2)、(2,2) 被错误清零（实际列 2 没有原生 0）。

       正确做法：从右下 (m-1, n-1) 开始，先处理内部格子，最后处理第一行/列：
         处理顺序示意（数字表示处理顺序，1最先，9最后）：
           j=0   1   2
        i=0 [9] [8] [7]   ← 第0行最后处理
        i=1 [6] [5] [4]
        i=2 [3] [2] [1]   ← (2,2) 最先处理
              ↑
         第0列在每个 i 的循环末尾单独处理（对应代码中 if first_col_zero）

         这样读取 matrix[0][j]、matrix[i][0] 时，标记都还在。

    【举例】matrix = [[0,1,2,0],[3,4,5,2],[1,3,1,5]]
      图示（完整流程）：
        原始（●=原生0）    标记后              置零后
          j=0 1 2 3        j=0 1 2 3           j=0 1 2 3
        i=0 [●][1][2][●]  i=0 [0][1][2][0]  i=0 [0][0][0][0]
        i=1 [3][4][5][2]  i=1 [0][4][5][2]  i=1 [0][4][5][0]
        i=2 [1][3][1][5]  i=2 [1][3][1][5]  i=2 [0][3][1][0]
                          ↑ [0][0]=0→行0   ↑ 第0列 first_col_zero
                          ↑ [0][3]=0→列3      第3列 根据[0][3]=0
       first_col_zero=True（matrix[0][0]=0 即第0列有原生0）

    【时间复杂度】O(m*n)
    【空间复杂度】O(1)
    """
    def setZeroes_inplace(self, matrix: List[List[int]]) -> None:
        m, n = len(matrix), len(matrix[0])
        # first_col_zero 表示第0列是否需要置零
        first_col_zero = False

        for i in range(m):
            if matrix[i][0] == 0:
                first_col_zero = True
            for j in range(1, n):
                if matrix[i][j] == 0:
                    matrix[i][0] = 0
                    matrix[0][j] = 0

        for i in range(m - 1, -1, -1):
            for j in range(n - 1, 0, -1):
                if matrix[i][0] == 0 or matrix[0][j] == 0:
                    matrix[i][j] = 0
            if first_col_zero:
                matrix[i][0] = 0
