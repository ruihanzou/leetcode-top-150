"""
LeetCode 48. Rotate Image
难度: Medium

题目描述：
给定一个 n × n 的二维矩阵 matrix 表示一个图像，请你将图像顺时针旋转 90 度。
你必须在原地旋转图像，这意味着你需要直接修改输入的二维矩阵。请不要使用另一个矩阵来旋转图像。

示例 1：
输入：matrix = [[1,2,3],[4,5,6],[7,8,9]]
输出：[[7,4,1],[8,5,2],[9,6,3]]

示例 2：
输入：matrix = [[5,1,9,11],[2,4,8,10],[13,3,6,7],[15,14,12,16]]
输出：[[15,13,2,5],[14,3,4,1],[12,6,8,9],[16,7,10,11]]

【拓展练习】
1. LeetCode 2048. Next Greater Numerically Balanced Number —— 矩阵变换相关的思维题
2. LeetCode 1886. Determine Whether Matrix Can Be Obtained By Rotation —— 判断矩阵是否可由旋转得到
3. LeetCode 867. Transpose Matrix —— 矩阵转置的基础操作
"""

from typing import List


class Solution:
    """
    ==================== 解法一：转置 + 水平翻转 ====================

    【核心思路】
    顺时针旋转90度 = 先沿主对角线转置 + 再左右翻转每一行。
    数学推导：旋转后 matrix[i][j] → matrix[j][n-1-i]
    分解为：转置 (i,j)→(j,i)，再左右翻转 (j,i)→(j,n-1-i)。

    【整体流程图示】
      原矩阵              转置（沿主对角线↘）      左右翻转（= 顺时针90°）
    1 2 3         1 4 7         7 4 1
    4 5 6    →    2 5 8    →     8 5 2
    7 8 9         3 6 9         9 6 3

    【转置详解】为什么 for j in range(i + 1, n)？
    转置需要交换 matrix[i][j] ↔ matrix[j][i]。
    若遍历所有 (i,j)，每个格子会交换两次（(0,1)↔(1,0) 后又 (1,0)↔(0,1)）→ 等于没换。
    因此只遍历 j > i 的「上三角」，每个格子只交换一次。

    遍历范围图示（n=3，✓=需交换，-=跳过）：
               j=0   j=1   j=2
            +-----+-----+-----+
         i=0|  -  |  ✓  |  ✓  |   ← 主对角线不换，上三角才换
            +-----+-----+-----+
         i=1|  -  |  -  |  ✓  |
            +-----+-----+-----+
         i=2|  -  |  -  |  -  |
            +-----+-----+-----+
    下三角 (j<i) 不需要遍历：交换 (i,j) 时已经顺带处理了 (j,i)。

    转置交换顺序示意（按 i=0,1,2 依次处理）：
      i=0,j=1: (0,1)↔(1,0)  即 2↔4      i=0,j=2: (0,2)↔(2,0)  即 3↔7
      i=1,j=2: (1,2)↔(2,1)  即 6↔8

    【左右翻转】matrix[i].reverse()
    每行内列 0↔n-1、1↔n-2 互换。n=3 时，以第 0 行为例：
      转置后 [1,4,7]  →  reverse  →  [7,4,1]

      翻转图示（以第 0 行 [1,4,7] 为例）：
      翻转前:  [1] [4] [7]    列0 列1 列2
      翻转后:  [7] [4] [1]    swap(0,2)，列1 不动
              ←——对称交换——→

    【时间复杂度】O(n²)
    【空间复杂度】O(1)
    """
    def rotate_transpose(self, matrix: List[List[int]]) -> None:
        n = len(matrix)

        for i in range(n):
            for j in range(i + 1, n):
                matrix[i][j], matrix[j][i] = matrix[j][i], matrix[i][j]

        for i in range(n):
            matrix[i].reverse()

    """
    ==================== 解法二：四点轮换 ====================

    【核心思路】
    直接处理旋转：每次将四个对应位置的元素进行循环交换。
    位置关系：(i,j) → (j,n-1-i) → (n-1-i,n-1-j) → (n-1-j,i) → (i,j)

    【思考过程】
    1. 顺时针旋转90度时，四个位置形成一个循环：
       (i, j) 的值移到 (j, n-1-i)
       (j, n-1-i) 的值移到 (n-1-i, n-1-j)
       (n-1-i, n-1-j) 的值移到 (n-1-j, i)
       (n-1-j, i) 的值移到 (i, j)

    2. Python 支持多元赋值，可以一行完成四个位置的轮换，无需临时变量。

    3. 遍历范围：外层 i ∈ [0, n//2)，内层 j ∈ [i, n-1-i)。
       这恰好覆盖了矩阵的"左上角四分之一"（不重不漏）。

    【举例】n=3, i=0, j=0
      四个位置：(0,0), (0,2), (2,2), (2,0)
      值分别为：1, 3, 9, 7
      轮换后：  7, 1, 3, 9

      i=0, j=1
      四个位置：(0,1), (1,2), (2,1), (1,0)
      值分别为：2, 6, 8, 4
      轮换后：  4, 2, 6, 8

      中心(1,1)=5不动。最终：[[7,4,1],[8,5,2],[9,6,3]] ✓

    【时间复杂度】O(n²)
    【空间复杂度】O(1)
    """
    def rotate_fourWay(self, matrix: List[List[int]]) -> None:
        n = len(matrix)

        for i in range(n // 2):
            for j in range(i, n - 1 - i):
                (matrix[i][j],
                 matrix[j][n - 1 - i],
                 matrix[n - 1 - i][n - 1 - j],
                 matrix[n - 1 - j][i]) = (
                    matrix[n - 1 - j][i],
                    matrix[i][j],
                    matrix[j][n - 1 - i],
                    matrix[n - 1 - i][n - 1 - j])
