/**
 * LeetCode 69. Sqrt(x)
 * 难度: Easy
 *
 * 题目描述：
 * 给你一个非负整数 x，计算并返回 x 的算术平方根。
 * 由于返回类型是整数，结果只保留整数部分，小数部分将被舍去。
 * 注意：不允许使用任何内置指数函数和算符，例如 pow(x, 0.5) 或者 x ** 0.5。
 *
 * 示例：x = 8 → 2（√8 = 2.828...，取整数部分 2）
 *
 * 【拓展练习】
 * 1. LeetCode 367. Valid Perfect Square —— 判断是否是完全平方数
 * 2. LeetCode 633. Sum of Square Numbers —— 两个平方数之和
 * 3. LeetCode 50. Pow(x, n) —— 快速幂的实现
 */

class Sqrt069 {

    /**
     * ==================== 解法一：二分查找 ====================
     *
     * 【核心思路】
     * 在 [0, x] 范围内二分查找，找到最大的 mid 使得 mid*mid <= x。
     *
     * 【思考过程】
     * 1. 目标：找最大的整数 k 满足 k² ≤ x。
     *    k 的范围是 [0, x]（实际上 [0, x/2+1] 就够了，但 [0, x] 也可以）。
     *
     * 2. k² 是单调递增的，所以可以二分。
     *    - 如果 mid² ≤ x，说明 mid 可能是答案，继续往右找更大的 → lo = mid + 1
     *    - 如果 mid² > x，说明 mid 太大 → hi = mid - 1
     *
     * 3. 注意 mid*mid 可能溢出 int，用 long 来计算。
     *
     * 【举例】x = 8
     *   lo=0, hi=8
     *   mid=4: 4*4=16 > 8  → hi=3
     *   mid=1: 1*1=1 <= 8  → ans=1, lo=2
     *   mid=2: 2*2=4 <= 8  → ans=2, lo=3
     *   mid=3: 3*3=9 > 8   → hi=2
     *   lo>hi，返回 ans=2
     *
     * 【时间复杂度】O(log x)
     * 【空间复杂度】O(1)
     */
    public int mySqrtBinary(int x) {
        if (x < 2) return x;

        int lo = 1, hi = x / 2, ans = 1;

        while (lo <= hi) {
            int mid = lo + (hi - lo) / 2;
            long square = (long) mid * mid;
            if (square <= x) {
                ans = mid;
                lo = mid + 1;
            } else {
                hi = mid - 1;
            }
        }

        return ans;
    }

    /**
     * ==================== 解法二：牛顿迭代法 ====================
     *
     * 【核心思路】
     * 牛顿法求 f(k) = k² - x = 0 的根。
     * 迭代公式：k_{n+1} = (k_n + x / k_n) / 2
     * 当 k 不再变化（或 k² ≤ x < (k+1)²）时停止。
     *
     * 【思考过程】
     * 1. 牛顿法是求方程根的经典数值方法。
     *    对于 f(k) = k² - x，导数 f'(k) = 2k。
     *    牛顿迭代公式：k_{n+1} = k_n - f(k_n)/f'(k_n)
     *                          = k_n - (k_n² - x)/(2k_n)
     *                          = (k_n + x/k_n) / 2
     *
     * 2. 从初始猜测 k = x 开始（或 x/2），不断迭代。
     *    当 k² ≤ x 时停止，此时 k 就是答案。
     *
     * 3. 牛顿法收敛速度是二次的（每次迭代有效位数翻倍），
     *    实际上只需要很少的迭代次数。
     *
     * 【举例】x = 8
     *   k=8:  k = (8 + 8/8) / 2 = (8+1)/2 = 4
     *   k=4:  k = (4 + 8/4) / 2 = (4+2)/2 = 3
     *   k=3:  k = (3 + 8/3) / 2 = (3+2)/2 = 2
     *   k=2:  2*2=4 <= 8 ✓，停止，返回 2
     *
     * 【时间复杂度】O(log x) —— 牛顿法二次收敛，实际迭代次数极少
     * 【空间复杂度】O(1)
     */
    public int mySqrtNewton(int x) {
        if (x < 2) return x;

        long k = x;
        while (k * k > x) {
            k = (k + x / k) / 2;
        }

        return (int) k;
    }

    /**
     * ==================== 解法三：内置函数 ====================
     *
     * 【核心思路】
     * 直接使用 Math.sqrt()，取整数部分。
     *
     * 【思考过程】
     * 1. 虽然题目说不允许用内置指数函数，但了解这个方法有助于验证。
     * 2. Math.sqrt 返回 double，强转 int 即可截断小数部分。
     * 3. 面试中通常需要手写实现，不能直接调库。
     *
     * 【时间复杂度】O(1) —— 硬件指令级别
     * 【空间复杂度】O(1)
     */
    public int mySqrtBuiltin(int x) {
        return (int) Math.sqrt(x);
    }
}
