/**
 * LeetCode 191. Number of 1 Bits
 * 难度: Easy
 *
 * 题目描述：
 * 编写一个函数，输入是一个无符号整数（以二进制串的形式），
 * 返回其二进制表达式中数字位数为 '1' 的个数（也被称为汉明权重）。
 *
 * 示例 1：n = 0b00000000000000000000000000001011 → 输出 3
 * 示例 2：n = 0b00000000000000000000000010000000 → 输出 1
 * 示例 3：n = 0b11111111111111111111111111111101 → 输出 31
 *
 * 【拓展练习】
 * 1. LeetCode 338. Counting Bits —— 统计 0 到 n 每个数的二进制 1 的个数，DP
 * 2. LeetCode 190. Reverse Bits —— 颠倒二进制位，逐位操作
 * 3. LeetCode 461. Hamming Distance —— 两个数的汉明距离，异或后数 1
 */

class NumberOf1Bits191 {

    /**
     * ==================== 解法一：n & (n-1) 消除最低位 1 ====================
     *
     * 【核心思路】
     * 利用位运算技巧 n & (n-1) 可以消除 n 最低位的 1。
     * 每次执行该操作，计数器加 1，直到 n 变为 0。
     * 计数器的值就是 1 的个数。
     *
     * 【思考过程】
     * 1. n & (n-1) 为什么能消除最低位的 1？
     *    - n 的最低位 1 在某个位置 k，则 n 的第 k 位是 1，第 0~k-1 位全是 0。
     *    - n-1 会把第 k 位变成 0，第 0~k-1 位全变成 1（借位效应）。
     *    - n & (n-1) 就把第 k 位的 1 消掉了，更高位不受影响。
     * 2. 每消掉一个 1，计数加 1，直到 n=0 为止。
     * 3. 循环次数 = 1 的个数，比逐位检查更高效。
     *
     * 【举例】n = 0b1011 (11)
     *   n=1011, n-1=1010 → n&(n-1)=1010, count=1（消掉最低位的1）
     *   n=1010, n-1=1001 → n&(n-1)=1000, count=2（消掉倒数第二个1）
     *   n=1000, n-1=0111 → n&(n-1)=0000, count=3（消掉最高位的1）
     *   n=0，结束，答案 = 3
     *
     * 【时间复杂度】O(k)，k 是二进制中 1 的个数
     * 【空间复杂度】O(1)
     */
    public int hammingWeightBrian(int n) {
        int count = 0;
        while (n != 0) {
            n &= (n - 1);
            count++;
        }
        return count;
    }

    /**
     * ==================== 解法二：逐位检查 ====================
     *
     * 【核心思路】
     * 从最低位开始，逐位检查每一位是否为 1。
     * 用 n & 1 取最低位，然后无符号右移一位，重复 32 次。
     *
     * 【思考过程】
     * 1. 最直观的方法：一位一位地看。
     * 2. n & 1 检查最低位是否为 1。
     * 3. n >>>= 1（无符号右移）把 n 右移一位，让下一位变成最低位。
     * 4. 固定循环 32 次（32 位整数）。
     * 5. Java 中必须用 >>> 无符号右移，否则负数的符号位会不断扩展导致死循环。
     *
     * 【举例】n = 0b1011 (11)（简化为4位）
     *   位0: 1011 & 1 = 1 → count=1, n=0101
     *   位1: 0101 & 1 = 1 → count=2, n=0010
     *   位2: 0010 & 1 = 0 → count=2, n=0001
     *   位3: 0001 & 1 = 1 → count=3, n=0000
     *   答案 = 3
     *
     * 【时间复杂度】O(1)，固定 32 次
     * 【空间复杂度】O(1)
     */
    public int hammingWeightCheck(int n) {
        int count = 0;
        for (int i = 0; i < 32; i++) {
            count += (n & 1);
            n >>>= 1;
        }
        return count;
    }
}
