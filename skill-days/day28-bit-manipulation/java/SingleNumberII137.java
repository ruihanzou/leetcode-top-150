/**
 * LeetCode 137. Single Number II
 * 难度: Medium
 *
 * 题目描述：
 * 给你一个整数数组 nums，除某个元素仅出现一次外，其余每个元素都恰出现三次。
 * 请你找出并返回那个只出现了一次的元素。
 * 你必须设计并实现线性时间复杂度的算法且使用常量级别的额外空间来解决此问题。
 *
 * 示例 1：nums = [2,2,3,2] → 输出 3
 * 示例 2：nums = [0,1,0,1,0,1,99] → 输出 99
 *
 * 【拓展练习】
 * 1. LeetCode 136. Single Number —— 其余元素出现两次，异或即可
 * 2. LeetCode 260. Single Number III —— 有两个只出现一次的元素，异或+分组
 * 3. LeetCode 645. Set Mismatch —— 找到重复和缺失的数字
 */

class SingleNumberII137 {

    /**
     * ==================== 解法一：逐位统计 ====================
     *
     * 【核心思路】
     * 对于 32 位整数的每一位，统计数组中所有元素在该位上 1 的个数，
     * 对 3 取模。如果余数为 1，说明那个只出现一次的数在该位上是 1。
     * 逐位还原出结果。
     *
     * 【思考过程】
     * 1. 异或能处理"出现两次抵消"，但无法直接处理"出现三次抵消"。
     * 2. 换个角度：如果某一位上出现了 3k+1 个 1，那多出来的那个 1
     *    一定来自只出现一次的数。
     * 3. 遍历 32 位，每一位上统计所有数在该位的 1 的个数，mod 3 得到结果的该位。
     *
     * 【举例】nums = [2, 2, 3, 2]
     *   二进制：2=10, 2=10, 3=11, 2=10
     *   第0位: 0+0+1+0 = 1, 1%3 = 1 → 结果第0位=1
     *   第1位: 1+1+1+1 = 4, 4%3 = 1 → 结果第1位=1
     *   结果 = 0b11 = 3
     *
     * 【时间复杂度】O(32n) = O(n)
     * 【空间复杂度】O(1)
     */
    public int singleNumberBitCount(int[] nums) {
        int result = 0;
        for (int i = 0; i < 32; i++) {
            int bitSum = 0;
            for (int num : nums) {
                bitSum += (num >> i) & 1;
            }
            if (bitSum % 3 != 0) {
                result |= (1 << i);
            }
        }
        return result;
    }

    /**
     * ==================== 解法二：状态机（有限自动机） ====================
     *
     * 【核心思路】
     * 用两个变量 ones 和 twos 来记录每一位上 1 出现次数 mod 3 的状态。
     * ones 记录"出现次数 mod 3 = 1"的位，twos 记录"出现次数 mod 3 = 2"的位。
     * 当某一位上 1 出现了 3 次，ones 和 twos 的该位都清零。
     *
     * 【思考过程】
     * 1. 对于每一位，1 的计数状态有 3 种：0次、1次、2次（mod 3）。
     *    用两个比特 (twos, ones) 编码：00→0次，01→1次，10→2次。
     *
     * 2. 状态转移（当输入一个新的 bit）：
     *    (0,0) + 1 → (0,1)   0次→1次
     *    (0,1) + 1 → (1,0)   1次→2次
     *    (1,0) + 1 → (0,0)   2次→0次（3次归零）
     *    输入0时状态不变。
     *
     * 3. 转移公式推导：
     *    ones = (ones ^ num) & ~twos
     *    twos = (twos ^ num) & ~ones
     *    这是对所有 32 位并行执行的。
     *
     * 4. 最终 ones 中就存着"出现 1 次（mod 3）"的那些位，即答案。
     *
     * 【举例】nums = [2, 2, 3, 2]，二进制 2=10, 3=11
     *   初始: ones=00, twos=00
     *   num=2(10): ones=(00^10)&~00=10, twos=(00^10)&~10=00
     *              → ones=10, twos=00 (第1位出现1次)
     *   num=2(10): ones=(10^10)&~00=00, twos=(00^10)&~00=10
     *              → ones=00, twos=10 (第1位出现2次)
     *   num=3(11): ones=(00^11)&~10=01, twos=(10^11)&~01=00
     *              → ones=01, twos=00 (第1位3次归零，第0位1次)
     *   num=2(10): ones=(01^10)&~00=11, twos=(00^10)&~11=00
     *              → ones=11=3，答案正确！
     *
     * 【时间复杂度】O(n)，只需遍历一次数组
     * 【空间复杂度】O(1)
     */
    public int singleNumberState(int[] nums) {
        int ones = 0, twos = 0;
        for (int num : nums) {
            ones = (ones ^ num) & ~twos;
            twos = (twos ^ num) & ~ones;
        }
        return ones;
    }
}
