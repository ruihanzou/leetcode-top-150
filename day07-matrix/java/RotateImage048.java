/**
 * LeetCode 48. Rotate Image
 * 难度: Medium
 *
 * 题目描述：
 * 给定一个 n × n 的二维矩阵 matrix 表示一个图像，请你将图像顺时针旋转 90 度。
 * 你必须在原地旋转图像，这意味着你需要直接修改输入的二维矩阵。请不要使用另一个矩阵来旋转图像。
 *
 * 示例 1：
 * 输入：matrix = [[1,2,3],[4,5,6],[7,8,9]]
 * 输出：[[7,4,1],[8,5,2],[9,6,3]]
 *
 * 示例 2：
 * 输入：matrix = [[5,1,9,11],[2,4,8,10],[13,3,6,7],[15,14,12,16]]
 * 输出：[[15,13,2,5],[14,3,4,1],[12,6,8,9],[16,7,10,11]]
 *
 * 【拓展练习】
 * 1. LeetCode 2048. Next Greater Numerically Balanced Number —— 矩阵变换相关的思维题
 * 2. LeetCode 1886. Determine Whether Matrix Can Be Obtained By Rotation —— 判断矩阵是否可由旋转得到
 * 3. LeetCode 867. Transpose Matrix —— 矩阵转置的基础操作
 */

class RotateImage048 {

    /**
     * ==================== 解法一：转置 + 水平翻转 ====================
     *
     * 【核心思路】
     * 顺时针旋转90度 = 先沿主对角线转置 + 再左右翻转每一行。
     * 数学推导：旋转后 matrix[i][j] → matrix[j][n-1-i]
     * 分解为：转置 (i,j)→(j,i)，再左右翻转 (j,i)→(j,n-1-i)。
     *
     * 【思考过程】
     * 1. 直接想旋转涉及四个位置的轮换，比较复杂。
     *    能否把旋转分解为更简单的操作？
     *
     * 2. 线性代数告诉我们：顺时针旋转90° = 转置 × 左右翻转。
     *    转置：行列互换，matrix[i][j] ↔ matrix[j][i]
     *    左右翻转：每行首尾对称交换
     *
     * 3. 转置只需遍历上三角（i < j），交换 (i,j) 和 (j,i)。
     *    左右翻转只需遍历每行前半部分。
     *    两步都是原地操作，O(1) 额外空间。
     *
     * 【举例】matrix = [[1,2,3],[4,5,6],[7,8,9]]
     *   转置后：[[1,4,7],[2,5,8],[3,6,9]]
     *   左右翻转后：[[7,4,1],[8,5,2],[9,6,3]] ✓
     *
     * 【时间复杂度】O(n²)
     * 【空间复杂度】O(1)
     */
    public void rotate_transpose(int[][] matrix) {
        int n = matrix.length;

        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                int tmp = matrix[i][j];
                matrix[i][j] = matrix[j][i];
                matrix[j][i] = tmp;
            }
        }

        for (int i = 0; i < n; i++) {
            for (int lo = 0, hi = n - 1; lo < hi; lo++, hi--) {
                int tmp = matrix[i][lo];
                matrix[i][lo] = matrix[i][hi];
                matrix[i][hi] = tmp;
            }
        }
    }

    /**
     * ==================== 解法二：四点轮换 ====================
     *
     * 【核心思路】
     * 直接处理旋转：每次将四个对应位置的元素进行循环交换。
     * 位置关系：(i,j) → (j,n-1-i) → (n-1-i,n-1-j) → (n-1-j,i) → (i,j)
     *
     * 【思考过程】
     * 1. 顺时针旋转90度时，四个位置形成一个循环：
     *    (i, j) 的值移到 (j, n-1-i)
     *    (j, n-1-i) 的值移到 (n-1-i, n-1-j)
     *    (n-1-i, n-1-j) 的值移到 (n-1-j, i)
     *    (n-1-j, i) 的值移到 (i, j)
     *
     * 2. 只需一个临时变量就能完成四个位置的轮换。
     *
     * 3. 遍历范围：外层 i ∈ [0, n/2)，内层 j ∈ [i, n-1-i)。
     *    这恰好覆盖了矩阵的"左上角四分之一"（不重不漏）。
     *
     * 【举例】n=3, i=0, j=0
     *   四个位置：(0,0), (0,2), (2,2), (2,0)
     *   值分别为：1, 3, 9, 7
     *   轮换后：  7, 1, 3, 9
     *
     *   i=0, j=1
     *   四个位置：(0,1), (1,2), (2,1), (1,0)
     *   值分别为：2, 6, 8, 4
     *   轮换后：  4, 2, 6, 8
     *
     *   中心(1,1)=5不动。最终：[[7,4,1],[8,5,2],[9,6,3]] ✓
     *
     * 【时间复杂度】O(n²)
     * 【空间复杂度】O(1)
     */
    public void rotate_fourWay(int[][] matrix) {
        int n = matrix.length;

        for (int i = 0; i < n / 2; i++) {
            for (int j = i; j < n - 1 - i; j++) {
                int tmp = matrix[i][j];
                matrix[i][j] = matrix[n - 1 - j][i];
                matrix[n - 1 - j][i] = matrix[n - 1 - i][n - 1 - j];
                matrix[n - 1 - i][n - 1 - j] = matrix[j][n - 1 - i];
                matrix[j][n - 1 - i] = tmp;
            }
        }
    }
}
