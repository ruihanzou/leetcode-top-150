/**
 * LeetCode 66. Plus One
 * 难度: Easy
 *
 * 题目描述：
 * 给定一个由整数组成的非空数组所表示的非负整数，在该数的基础上加一。
 * 最高位数字存放在数组的首位，每个元素只存储单个数字。
 * 你可以假设除了整数 0 之外，这个整数不会以零开头。
 *
 * 示例：digits = [1,2,3] → [1,2,4]，digits = [9,9,9] → [1,0,0,0]
 *
 * 【拓展练习】
 * 1. LeetCode 67. Add Binary —— 二进制加法，同样的进位思路
 * 2. LeetCode 2. Add Two Numbers —— 链表形式的加法
 * 3. LeetCode 369. Plus One Linked List —— 链表版本的加一
 */

class PlusOne066 {

    /**
     * ==================== 解法一：从低位到高位进位 ====================
     *
     * 【核心思路】
     * 从最低位开始加 1，如果产生进位就继续向高位传播。
     * 如果某一位加 1 后不产生进位（<10），可以直接返回。
     * 如果所有位都产生了进位（如 999→1000），需要扩展数组。
     *
     * 【思考过程】
     * 1. 加一操作只会在当前位是 9 的时候产生进位。
     *    如果当前位不是 9，加完直接返回就行，不需要继续处理。
     *
     * 2. 如果当前位是 9，加 1 变成 10，当前位变 0，进位传给上一位。
     *    这个过程可能连锁：99 → 100，999 → 1000。
     *
     * 3. 只有当所有位都是 9 时，才需要扩展数组长度。
     *    此时结果一定是 10...0（首位1，其余全0），直接构造即可。
     *
     * 【举例】digits = [1,2,9]
     *   i=2: digits[2]=9, 9+1=10, 置0 → [1,2,0], carry继续
     *   i=1: digits[1]=2, 2+1=3, 无进位 → [1,3,0], 返回
     *
     *   digits = [9,9,9]
     *   i=2: 9→0, i=1: 9→0, i=0: 9→0
     *   循环结束仍有进位 → 新建 [1,0,0,0]
     *
     * 【时间复杂度】O(n) —— 最坏情况遍历所有位
     * 【空间复杂度】O(1) —— 不算输出数组，原地修改（扩展时 O(n)）
     */
    public int[] plusOneCarry(int[] digits) {
        for (int i = digits.length - 1; i >= 0; i--) {
            digits[i]++;
            if (digits[i] < 10) {
                return digits;
            }
            digits[i] = 0;
        }

        int[] result = new int[digits.length + 1];
        result[0] = 1;
        return result;
    }

    /**
     * ==================== 解法二：找最右边非9的位 ====================
     *
     * 【核心思路】
     * 找到从右往左第一个不是 9 的数字，将其 +1，右边所有 9 变 0。
     * 如果所有数字都是 9，直接构造 10...0。
     *
     * 【思考过程】
     * 1. 加一的效果本质上就是：从最低位开始，连续的 9 全变 0，
     *    第一个非 9 的数字 +1。这与解法一本质相同，但换了一种表述。
     *
     * 2. 先从右往左扫一遍，找到第一个不是 9 的位置 i。
     *    - 将 digits[i] += 1
     *    - 将 i 右边所有位置 0
     *    - 返回
     *
     * 3. 如果找不到非 9 的位（全是 9），说明需要进位到新的最高位。
     *
     * 【举例】digits = [2,9,9]
     *   从右往左找第一个非9: digits[0]=2
     *   digits[0]++ → 3, 右边全置0 → [3,0,0]
     *
     * 【时间复杂度】O(n)
     * 【空间复杂度】O(1)（不算输出数组）
     */
    public int[] plusOneFindNonNine(int[] digits) {
        for (int i = digits.length - 1; i >= 0; i--) {
            if (digits[i] != 9) {
                digits[i]++;
                for (int j = i + 1; j < digits.length; j++) {
                    digits[j] = 0;
                }
                return digits;
            }
        }

        int[] result = new int[digits.length + 1];
        result[0] = 1;
        return result;
    }
}
