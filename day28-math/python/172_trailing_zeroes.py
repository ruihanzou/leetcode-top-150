"""
LeetCode 172. Factorial Trailing Zeroes
难度: Medium

题目描述：
给定一个整数 n，返回 n! 结果中尾随零的数量。
提示：n! = n × (n-1) × (n-2) × ... × 3 × 2 × 1

示例：n = 5 → 1（5! = 120，1个尾随零），n = 10 → 2（10! = 3628800，2个尾随零）

【拓展练习】
1. LeetCode 793. Preimage Size of Factorial Zeroes Function —— 反向问题：给定尾零数量求n的范围
2. LeetCode 233. Number of Digit One —— 类似的数学规律题
3. LeetCode 1401. Circle and Rectangle Overlapping —— 数学分析能力练习
"""


class Solution:
    """
    ==================== 解法一：统计因子5的个数 ====================

    【核心思路】
    尾随零由因子 10 产生，而 10 = 2 × 5。
    在 n! 的质因数分解中，因子 2 的个数远多于因子 5 的个数，
    所以尾随零的个数 = 因子 5 的个数 = n//5 + n//25 + n//125 + ...

    【思考过程】
    1. 每个尾随零对应一个因子 10，即需要一对 (2, 5)。
       在 1×2×3×...×n 中，偶数远多于 5 的倍数，
       所以因子 2 不会成为瓶颈，瓶颈是因子 5 的数量。

    2. 5 的倍数（5,10,15,...）每个贡献 1 个因子 5 → 共 n//5 个。
       25 的倍数（25,50,75,...）额外多贡献 1 个因子 5 → 共 n//25 个。
       125 的倍数额外再多贡献 1 个 → 共 n//125 个。
       以此类推……

    3. 总因子 5 的个数 = n//5 + n//25 + n//125 + n//625 + ...
       直到 5^k > n 时停止。

    【举例】n = 30
      30//5 = 6  (5,10,15,20,25,30 各贡献至少一个5)
      30//25 = 1 (25 额外贡献一个5)
      30//125 = 0 停止
      总计 = 6 + 1 = 7

      验证: 30! = 265252859812191058636308480000000 → 7个尾零 ✓

    【时间复杂度】O(log₅ n) —— 每次除以5
    【空间复杂度】O(1)
    """
    def trailingZeroes(self, n: int) -> int:
        count = 0
        while n >= 5:
            n //= 5
            count += n
        return count
